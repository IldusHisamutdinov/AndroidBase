public class WeatherData {

    public WeatherData(String empty, String day, String tempDay, String tempNight) {
        this.empty = empty;
        this.day = day;
        this.tempDay = tempDay;
        this.tempNight = tempNight;
    }

    public String empty;
    public String day;
    public String tempDay;
    public String tempNight;

}
